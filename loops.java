public class loops {
    public static void main(String[] args) {
        //1. for 반복문
        //초기화된 변수, 조건식, 증감식이 필요하다.
        for (int i=0;i<10;i++){
            System.out.println(i);
        }
        // 2. while 반복문
        int b = 10;
        //while( b>0){
        //    System.out.println(b);
        //    b--;
        //}
        do {
            System.out.println(b);
            b--;
        }while (b>0);

        }
    }

